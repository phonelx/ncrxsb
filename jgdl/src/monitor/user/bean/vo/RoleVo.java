package monitor.user.bean.vo;

/** 
 * 界面显示roleBean
 * @author  cl
 * @datetime  2011-4-20 下午02:53:51
 */
public class RoleVo {
	
	private int squ = -1;
	private String roleName = null;
	private String descb = null;
	private int isAdmin = 0;//是否是管理角色;1:管理角色 0：查询角色
	private int isSpr = 0;//是否是审批人角色；0：不是审批人；1：是审批人
	private String jsonStr = null;//前台传到后台的权限json字符串
	public String getRoleName() {
		return roleName;
	}
	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}
	public String getDescb() {
		return descb;
	}
	public void setDescb(String descb) {
		this.descb = descb;
	}
	public String getJsonStr() {
		return jsonStr;
	}
	public void setJsonStr(String jsonStr) {
		this.jsonStr = jsonStr;
	}
	public int getSqu() {
		return squ;
	}
	public void setSqu(String squ) {
		if(squ!=null){
			this.squ = Integer.parseInt(squ);
		}
	}
	public void setSqu(int squ) {
		this.squ = squ;
	}
	public int getIsAdmin() {
		return isAdmin;
	}
	public void setIsAdmin(int isAdmin) {
		this.isAdmin = isAdmin;
	}
	public void setIsAdmin(String isAdmin) {
		if(isAdmin!=null){
			this.isAdmin = Integer.parseInt(isAdmin);
		}
	}
	public int getIsSpr() {
		return isSpr;
	}
	public void setIsSpr(int isSpr) {
		this.isSpr = isSpr;
	}
	public void setIsSpr(String isSpr) {
		if(isSpr!=null){
			this.isSpr = Integer.parseInt(isSpr);
		}
	}
	
	
	
	
}
